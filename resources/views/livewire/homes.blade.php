            <div class="row mt-3">
                <p style="text-align: justify; text-indent: 0.5in;">
                    Lembaga Penelitian dan Pengabdian kepada Masyarakat Universitas Negeri Yogyakarta (LPPM UNY)
                    merupakan salah satu lembaga pelaksana akademik yang bertugas melaksanakan tugas pokok dan
                    fungsi
                    Universitas Negeri Yogyakarta dalam bidang penelitian, dan pengabdian kepada masyarakat. LPPM
                    UNY
                    berasal dari dua lembaga yang ada di Universitas Negeri Yogyakarta, yaitu Lembaga Penelitian dan
                    Lembaga Pengabdian Kepada Masyarakat. Kedua Lembaga didirikan berdasarkan Keputusan Mendikbud RI
                    Nomor 0101/O/1993 tanggal 27 Februari 1993 dan diperbaharui dengan Keputusan Mendikbud RI nomor
                    0174/O/1995 tanggal 18 Juli 1995 tentang Organisasi dan Tata Kerja Institut Keguruan dan Ilmu
                    Pendidikan Yogyakarta (IKIP Yogyakarta).
                </p>
                <p style="text-align: justify; text-indent: 0.5in;">Berdasarkan Keputusan Presiden R.I. nomor 93
                    tahun 1999 tanggal 4 Agustus 1999 IKIP Yogyakarta berubah menjadi Universitas Negeri Yogyakarta
                    (UNY). Hal itu diikuti oleh perangkat organisasi di bawahnya, Lembaga Penelitian Institut
                    Keguruan dan Ilmu Pendidikan (IKIP) Yogyakarta berubah menjadi Lembaga Penelitian Universitas
                    Negeri Yogyakarta, dan Lembaga Pengabdian Kepada Masyarakat IKIP Yogyakarta berganti menjadi
                    Lembaga Pengabdian Kepada Masyarakat Universitas Negeri Yogyakarta. Wider mandate ini membawa
                    konsekuensi pada penambahan dan perluasan bidang garapan penelitian dan pengabdian dalam bidang
                    nonkependidikan, walaupun tetap memiliki komitmen kuat terhadap bidang kependidikan. Keputusan
                    pemberian wider mandate ini diperkuat dengan surat keputusan Menteri Pendidikan Nasional
                    Republik Indonesia nomor SK Mendiknas No: 003/0/2001 tentang Statuta Universitas Negeri
                    Yogyakarta.</p>
                <p style="text-align: justify; text-indent: 0.5in;">Berdasarkan Permendiknas nomor 23 tahun 2011
                    tentang Organisasi dan Tata Kerja Universitas Negeri Yogyakarta, dan Permendiknas nomor 34 tahun
                    2011 tentang Statuta Universitas Negeri Yogyakarta, Lemlit UNY dan LPM UNY digabungkan menjadi
                    Lembaga Penelitian dan Pengabdian Kepada Masyarakat Universitas Negeri Yogyakarta disingkat
                    dengan LPPM UNY. Lembaga Penelitian dan Pengabdian Kepada Masyarakat mempunyai tugas
                    melaksanakan, mengkoordinasikan, memantau, dan menilai pelaksanaan kegiatan penelitian dan
                    pengabdian kepada masyarakat.</p>
                <p style="text-align: justify; text-indent: 0.5in;">Perubahan status kelembagaan tersebut mengubah
                    paradigma fungsi LPPM UNY tidak hanya sekedar melaksanakan fungsi utamanya, tetapi harus terus
                    berupaya melakukan berbagai inovasi penelitian dan pengabdian dalam rangka mendukung upaya UNY
                    menuju Universitas berkelas Dunia. untuk menopang pencapaian tersebut, tidak kalah pentingnya
                    adalah kerjasama dengan berbagai pihak dalam skala lokal, nasional, dan internasional. Perubahan
                    status dan kiprah LPPM UNY, menuntut agar visi dan misi yang harus dicapai oleh LPPM UNY selaras
                    dengan visi, misi, kebijakan arah pengembangan, dan program kerja universitas (UNY). Visi UNY
                    adalah pada tahun 2025 menjadi universitas kependidikan kelas dunia berlandaskan ketaqwaan,
                    kemandirian, dan kecendekiaan.</p>
